const supportedLanguages = ['id', 'en', 'zh'];

function getValueByPath(source, path) {
  return path.split('.').reduce((acc, segment) => acc?.[segment], source);
}

function interpolate(template, params = {}) {
  return String(template).replace(/\{(\w+)\}/g, (_, key) => params[key] ?? '');
}

export function getCurrentLanguage() {
  const storedLanguage = localStorage.getItem(LANGUAGE_STORAGE_KEY);
  if (storedLanguage && supportedLanguages.includes(storedLanguage)) {
    return storedLanguage;
  }
  return 'en';
}

export function getApiLanguage() {
  const language = getCurrentLanguage();
  if (language === 'en') return 'LANGUAGE_EN';
  if (language === 'zh') return 'LANGUAGE_ZH_HANS';
  return 'LANGUAGE_ID';
}

export function getLanguageOptions() {
  return [
    { value: 'en', label: 'EN', shortLabel: 'EN', flag: 'en' },
    { value: 'id', label: 'ID', shortLabel: 'ID', flag: 'id' },
    { value: 'zh', label: 'CN', shortLabel: 'CN', flag: 'zh' },
  ];
}

export function setCurrentLanguage(language) {
  const nextLanguage = supportedLanguages.includes(language) ? language : 'en';
  localStorage.setItem(LANGUAGE_STORAGE_KEY, nextLanguage);
  document.documentElement.lang = dictionaries[nextLanguage].app.langAttr;
  window.dispatchEvent(new CustomEvent('app:language-change', {
    detail: { language: nextLanguage },
  }));
}

export function initializeLanguage() {
  document.documentElement.lang = dictionaries[getCurrentLanguage()].app.langAttr;
}

export function t(path, params = {}) {
  const language = getCurrentLanguage();
  const localizedValue = getValueByPath(dictionaries[language], path);
  const fallbackValue = getValueByPath(dictionaries.id, path);
  const resolvedValue = localizedValue ?? fallbackValue ?? path;
  return interpolate(resolvedValue, params);
}
